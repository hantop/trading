    /**
     * 取得私钥
     *
     * @param keyMap
     * @return
     * @throws Exception
     */
    public static String getPrivateKey(Map<String, Object> keyMap)
            throws Exception {
        Key key = (Key) keyMap.get(PRIVATE_KEY);

        return encryptBASE64(key.getEncoded());
    }

    /**
     * 取得公钥
     *
     * @param keyMap
     * @return
     * @throws Exception
     */
    public static String getPublicKey(Map<String, Object> keyMap)
            throws Exception {
        Key key = (Key) keyMap.get(PUBLIC_KEY);

        return encryptBASE64(key.getEncoded());
    }

    /**
     * 初始化密钥
     *
     * @return
     * @throws Exception
     */
    public static Map<String, Object> initKey() throws Exception {
        KeyPairGenerator keyPairGen = KeyPairGenerator.getInstance(KEY_ALGORITHM);
        keyPairGen.initialize(1024);

        KeyPair keyPair = keyPairGen.generateKeyPair();

        // 公钥
        RSAPublicKey publicKey = (RSAPublicKey) keyPair.getPublic();

        // 私钥
        RSAPrivateKey privateKey = (RSAPrivateKey) keyPair.getPrivate();

        Map<String, Object> keyMap = new HashMap<>();

        keyMap.put(PUBLIC_KEY, publicKey);
        keyMap.put(PRIVATE_KEY, privateKey);
        return keyMap;
    }

    public static void main(String[] args) throws Exception {
        Map<String, Object> map = initKey();
        String privateKey = getPrivateKey(map);
        String publicKey = getPublicKey(map);
        System.out.println("私钥："+privateKey);
        System.out.println("公钥"+publicKey);

        String message = "hello world!";

        System.out.println("公钥加密：");
        byte[] bytes = encryptByPublicKey(message.getBytes("UTF-8"), publicKey);
        String encryptMessage= encryptBASE64(bytes);
        System.out.println(encryptMessage);

        long beginTime = System.currentTimeMillis();
        System.out.println("私钥解密：");
        byte[] bytes1 = decryptByPrivateKey(decryptBASE64(encryptMessage), privateKey);
        System.out.println(new String(bytes1));
        long time = System.currentTimeMillis() - beginTime;

        System.out.println(time+"ms");

        System.out.println("私钥加签，公钥验签");
        String sign = sign(message.getBytes("UTF-8"), privateKey);
        boolean verify = verify(message.getBytes("UTF-8"), publicKey, sign);
        System.out.println(verify);




    }
